    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        // TODO add your handling code here:

        char key = evt.getKeyChar();

        //space basınca ve henuz senaryo bıtmemısse           
        if (key == ' ' && game.nextValue != null && hamleIndex < sutunSenaryosu.length) {

            // Sütunu biz seçmiyoruz, diziden sırayla geliyor!
            //sıradakı sutunu alıyor bunu colındex yapıyır
            int colIndex = sutunSenaryosu[hamleIndex];
            hamleIndex++;

            //taşı secılen sutuna bırak 
            game.dropTile(colIndex);

            game.nextValue = game.generateNextValue();
            updateGUI(); //gorunumu guncelle 

            //merge ıslemı ıcın tımer kullanıyor 
            javax.swing.Timer timer = new javax.swing.Timer(500, null);
            timer.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
                    //bırlesecek tas varsa merged true 
                    boolean merged = game.merge(colIndex); //merge bırlesme olursa true döndürür
                    updateGUI(); //guı guncelle 
                    //bırlesecek tas yoksa tımer durdur yenı tas uret 
                    if (!merged) {
                        ((javax.swing.Timer) e.getSource()).stop();

                        updateGUI();
                    }
                }
            });
            timer.start();

        }


    }//GEN-LAST:event_formKeyPressed

