//degere gore labellerı renklendırır 
    private void renklendir(javax.swing.JLabel label, int value) {
        switch (value) {
            case 2:
                label.setBackground(Color.YELLOW);
                label.setForeground(Color.BLACK);
                break;
            case 4:
                label.setBackground(Color.ORANGE);
                break;
            case 8:
                label.setBackground(Color.GREEN);
                break;
            case 16:
                label.setBackground(Color.magenta);
                label.setForeground(Color.white);
                break;
            case 32:
                label.setBackground(Color.CYAN);
                label.setForeground(Color.BLACK);
                break;
            case 64:
                label.setBackground(Color.BLUE);
                label.setForeground(Color.white);
                break;
            case 128:
                label.setBackground(Color.RED);
                label.setForeground(Color.white);
                break;
            case 256:
                label.setBackground(Color.PINK);
                label.setForeground(Color.red);
                break;
            default:
                break;
        }

    }

    private void updateGUI() {
        temizle(0); //hucrelerı temızle

        //multı lınked lıstı headden baslayarak gez 
        NisaAktasNode<Integer> temp = game.head;
        while (temp != null) {
            //sol alt tumhucreler[0] yanı 1 2 3 4 
            //headin hangı tum hucreler[]indexı buldurup hangı labele gıttıgını bulduruyor 
            int labelIndex = ((6 - temp.row) * 5) + temp.col;

            //head ındexındekı labelın rengını textını degıstır 
            if (labelIndex >= 0 && labelIndex < tumHucreler.length) {
                tumHucreler[labelIndex].setText(temp.value.toString());
                renklendir(tumHucreler[labelIndex], temp.value);
                tumHucreler[labelIndex].setVisible(true);

            }
            temp = temp.next;
        }

        if (game.nextValue != null) {

            next_lbl.setText(game.nextValue.toString());
            next_lbl.setOpaque(true);
            renklendir(next_lbl, game.nextValue);
        } else if (!game_over_pnl.isVisible()) {
            next_lbl.setText(("GAME OVER"));

            if (!game_over_pnl.isVisible()) { //game over gorunur degılse 

                new javax.swing.Timer(2000, e -> { //2 sanıye sonra game over panelını goster 
                    game_over_pnl.setVisible(true);
                    game_over_pnl.requestFocus(); //space bastırmaz 
                    ((javax.swing.Timer) e.getSource()).stop(); //tımer durdurur 
                }).start();
            }
        }
        //yapıda değişiklik oldukça gunceletir 
        this.revalidate();
        this.repaint();

    }

    private void gameOverAyarları() {
        game_over_pnl.setBounds(0, 0, 800, 800); //boyut ayarı 
        game_over_pnl.setBackground(new java.awt.Color(0, 0, 0, 180));//panelın matlık ayarı
        game_over_pnl.setLayout(new java.awt.GridBagLayout());//yazı merkeze

        //bıtıs yazısı ayarı 
        javax.swing.JLabel bitisYazisi = new javax.swing.JLabel("GAME OVER");
        bitisYazisi.setFont(new java.awt.Font("Serif", java.awt.Font.BOLD, 70));
        bitisYazisi.setForeground(new java.awt.Color(236, 224, 201)); // 

        //  Yazıyı panele ekle
        game_over_pnl.add(bitisYazisi);

        //  Paneli en başta gizle
        game_over_pnl.setVisible(false);

        // 5. ÖNEMLİ: bitisyazisi'nin gridin üstünde görünmesi için LayeredPane 
        this.getLayeredPane().add(game_over_pnl, javax.swing.JLayeredPane.PALETTE_LAYER);
    }

